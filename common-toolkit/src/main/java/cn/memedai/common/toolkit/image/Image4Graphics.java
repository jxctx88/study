package cn.memedai.common.toolkit.image;

public class Image4Graphics {
	private String imagePath;//图片全路径
	private int x;//x轴
	private int y;//y轴
	private float clarity;//透明度
	public String getImagePath() {
		return imagePath;
	}
	public void setImagePath(String imagePath) {
		this.imagePath = imagePath;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public float getClarity() {
		return clarity;
	}
	public void setClarity(float clarity) {
		this.clarity = clarity;
	}
}
